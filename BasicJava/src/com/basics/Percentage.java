package com.basics;

public class Percentage {
	
	public static void main ( String [] args) {
		
		float num1 = 40;
		float num2=56;
		float num3=78;
		float totalMarks= 300;
		float obtainMarks= 0;
		
		
		float percentage = 0;
		obtainMarks= num1+num2+num3;
		
	percentage =( obtainMarks*100)/ totalMarks;
	System.out.println( percentage);
	
	}

}
